    private void prefillPatientData() {
        txtFirstname.setText(patient.getFirstname());
        txtLastname.setText(patient.getLastname());
        txtBirthdate.setText(patient.getBirthdate().toString());
        txtEmail.setText(patient.getEmail());
        txtPhone.setText(String.valueOf(patient.getPhone()));
        txtAddress.setText(patient.getAddress());
        txtUsername.setText(patient.getUsername());
        cmbGender.setSelectedItem(patient.isGender() ? "Male" : "Female");
    }

    private void loadDoctorComboBox() {
        cmbAttendingDoctor.removeAllItems();
        cmbAttendingDoctor.addItem("Select one");
        for (model.Doctor d : store.getDoctors()) {
            cmbAttendingDoctor.addItem(String.valueOf(d.getId()));
        }
    }

    private void loadRoomTypeComboBox() {
        cmbRoomType.removeAllItems();
        for (model.RoomType rt : model.RoomType.values()) {
            cmbRoomType.addItem(rt.name());
        }
    }

    private void loadAppointmentComboBox() {
        cmbCancelAppointmentId.removeAllItems();
        cmbCancelAppointmentId.addItem("Select one");
        for (model.Appointment a : patient.getAppointments()) {
            cmbCancelAppointmentId.addItem(a.getId());
        }
    }


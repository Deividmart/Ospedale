    private void btnSaveDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveDoctorActionPerformed
        RegisterDoctorController ctrl = new RegisterDoctorController();
        Response response = ctrl.register(
            user,
            txtDocId.getText(),
            txtDocUsername.getText(),
            txtDocFirstname.getText(),
            txtDocLastname.getText(),
            txtDocPassword.getText(),
            txtDocConfirmPwd.getText(),
            (String) cmbDocSpecialty.getSelectedItem(),
            txtDocLicence.getText(),
            txtDocOffice.getText()
        );
        if (response.isSuccess()) {
            JOptionPane.showMessageDialog(this, response.getMessage(), "Success", JOptionPane.INFORMATION_MESSAGE);
            txtDocFirstname.setText(""); txtDocLastname.setText(""); txtDocId.setText("");
            txtDocLicence.setText(""); txtDocOffice.setText(""); txtDocUsername.setText("");
            txtDocPassword.setText(""); txtDocConfirmPwd.setText("");
            cmbDocSpecialty.setSelectedIndex(0);
            loadDoctorComboBox();
        } else {
            JOptionPane.showMessageDialog(this, response.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveDoctorActionPerformed

    private void btnGoToDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoToDoctorActionPerformed
        String selected = (String) cmbSelectDoctor.getSelectedItem();
        if (selected == null || selected.equals("Select one")) {
            JOptionPane.showMessageDialog(this, "Please select a doctor", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        long doctorId = Long.parseLong(selected);
        model.Doctor doctor = (model.Doctor) store.findUserById(doctorId);
        if (doctor == null) {
            JOptionPane.showMessageDialog(this, "Doctor not found", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.setVisible(false);
        new DoctorView(user, doctor).setVisible(true);
    }//GEN-LAST:event_btnGoToDoctorActionPerformed


    private void btnGoToPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoToPatientActionPerformed
        String selected = (String) cmbSelectPatient.getSelectedItem();
        if (selected == null || selected.equals("Select one")) {
            JOptionPane.showMessageDialog(this, "Please select a patient", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        long patientId = Long.parseLong(selected);
        model.Patient patient = (model.Patient) store.findUserById(patientId);
        if (patient == null) {
            JOptionPane.showMessageDialog(this, "Patient not found", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.setVisible(false);
        new PatientView(user, patient).setVisible(true);
    }//GEN-LAST:event_btnGoToPatientActionPerformed


